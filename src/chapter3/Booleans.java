package chapter3;

public class Booleans {
    public static void main(String[] args) {
        boolean b1 = true;
        boolean b2 = false;

        System.out.println(b1);
        System.out.println(b2);

        int num1 = 10;
        int num2 = 12;

        System.out.println(num1 < num2); // prints true (boolean)
        System.out.println(num1 == 10); // prints true (boolean)
    }
}
